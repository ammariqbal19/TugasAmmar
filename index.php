            <h3>Tentang PSIS</h3>
            <p>
                        Sejarah tim sepak bola kota Semarang telah berlangsung sejak lama ketika kota ini masih berada di bawah kekuasaan pemerintah kolonial. Yang pertama tercatat adalah team sepak bola UNION yang berdiri tanggal 2 Juli 1911. UNION sendiri hanyalah sebutan bagi tim dengan nama Tionghoa Hoa Yoe Hwee Koan. Tim ini mendapatkan hak rechspersoon tahun 1917 dari pemerintah kolonial.[butuh rujukan
        Selanjutnya ada pula tim bernama Comite Kampioens-wedstrijden Tionghoa (CKTH) dengan gedung olahraga di wilayah Seteran. Pada tahun 1926 tim ini berubah nama menjadi Hwa Nan Voetbalbond (HNV). Tercatat klub Hwa Nan ini bahkan telah melakukan pertandingan eksibisi dengan klub luar negeri asal Taiwan, Loh Hua Team Voetbalbond.[butuh rujukan]
        Di kalangan pendukung pribumi, perkumpulan yang menonjol adalah Tots Ons Doel (TOD) yang didirikan pada 23 Mei 1928, bermarkas di Tanggul Kalibuntang (sekarang Jl. Dr. Cipto). Dalam perjalanannya Tots Ons Doel berganti nama menjadi PS. Sport Stal Spieren (SSS). PS SSS inilah yang kemudian menjadi cikal bakal PSIS Semarang. Pada tahun 1930 team ini berganti nama menjadi Voetbalbond Indonesia Semarang (VIS) yang berlatih di lapangan Karimata Timur.[butuh rujukan]
        Setelah PSSI lahir pada 19 April 1930, Voetbalbond Indonesia Semarang berganti nama penjadi Persatuan Sepak bola Indonesia Semarang (PSIS) yang beranggotakan klub sepak bola Romeo, PSKM, REA, MAS, PKVI, Naga, RIM, RDS dan SSS sendiri. Adapun nama klub SSS kemudian berganti menjadi berbahasa Indonesia, Sport Supaya Sehat, sampai sekarang.[butuh rujukan]
        == Prestasi . PSIS baru bisa mencicipi gelar juara pada tahun 1987. Kala itu PSIS Semarang yang diperkuat legenda besar, Ribut Waidi, mengalahkan Persebaya Surabaya di final kompetisi perserikatan PSSI dengan skor 1-0 melalui gol tunggal Syaiful Amri. Karena faktor terlalu cepat puas ini (apalagi ditambah keberhasilan punggawanya dalam merebut medali emas SEA Games yang pertama kali bagi Indonesia) maka di kompetisi berikutnya PSIS nyaris terjerumus dalam lubang degradasi ditambah dengan "campur tangan" Persebaya yang bermain untuk kalah 12-0 dari Persipura Jayapura. Untung saja PSIS masih mampu bertahan dan terus bertahan dengan peringkat tim medioker.
        Prestasi tertinggi PSIS adalah ketika menjuarai Kompetisi Divisi Utama Perserikatan PSSI tahun 1987 dan Juara Liga Indonesia 1999. Pada musim 2006 PSIS menjadi runner-up Liga Indonesia dengan keberhasilan mencapai final Liga Indonesia, berhadapan dengan Persik Kediri di Stadion Manahan, Solo dan kalah melalui akhir perpanjangan waktu babak ke-2. Saat ini PSIS Semarang juga berstatus sebagai runner-up Piala Emas Bang Yos (PEBY) yang terakhir, diadakan di Jakarta akhir tahun 2006.
